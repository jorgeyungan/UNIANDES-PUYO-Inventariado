
package ec.org.uniandes.beans;

import ec.org.uniandes.entidades.clsEquipo;
import ec.org.uniandes.entidades.clsPerifericos;
import ec.org.uniandes.funciones.crudPerifericos;
import java.util.List;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.RequestScoped;

@ManagedBean
@RequestScoped
public class ccPeriferico {
    private clsPerifericos newPeriferico;
    private clsPerifericos selectedPeriferico;
    private List<clsPerifericos> lista;
    private clsEquipo selectedequipo;

    public ccPeriferico() {
        newPeriferico=new clsPerifericos();
        cargarDatos();
    }
    private void cargarDatos(){
        lista=crudPerifericos.findbyAll();
    }
    public void insertar(){
        newPeriferico.setIdequipo(selectedequipo);
        if (crudPerifericos.save(newPeriferico)) {
            newPeriferico=new clsPerifericos();
            cargarDatos();
        } else {
        }
    }
    public void actualizar(){
        if (crudPerifericos.save(selectedPeriferico)) {
            newPeriferico=new clsPerifericos();
            cargarDatos();
        } else {
        }
    }
    public void eliminar(){
        if (crudPerifericos.save(selectedPeriferico)) {
            newPeriferico=new clsPerifericos();
            cargarDatos();
        } else {
        }
    }

    public clsPerifericos getNewPeriferico() {
        return newPeriferico;
    }

    public void setNewPeriferico(clsPerifericos newPeriferico) {
        this.newPeriferico = newPeriferico;
    }

    public clsPerifericos getSelectedPeriferico() {
        return selectedPeriferico;
    }

    public void setSelectedPeriferico(clsPerifericos selectedPeriferico) {
        this.selectedPeriferico = selectedPeriferico;
    }

    public List<clsPerifericos> getLista() {
        return lista;
    }

    public void setLista(List<clsPerifericos> lista) {
        this.lista = lista;
    }

    public clsEquipo getSelectedequipo() {
        return selectedequipo;
    }

    public void setSelectedequipo(clsEquipo selectedequipo) {
        this.selectedequipo = selectedequipo;
    }
    
    
}
